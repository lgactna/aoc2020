def solve(input, x_shift, y_shift):
    rows = input.split("\n")

    current_columns = len(rows[0])

    rows_used = (len(rows)//y_shift)+1

    columns_needed = len(rows)*x_shift

    string_multiplier = (columns_needed//current_columns)+1

    for index, row in enumerate(rows):
        rows[index] = row*string_multiplier

    trees_killed = 0

    for row_number in range(0, len(rows), y_shift):
        #print(f"tested ({row_number}, {int(row_number/y_shift)*x_shift})")
        if rows[row_number][int(row_number/y_shift)*x_shift] == "#":
            trees_killed += 1

    return(trees_killed)

